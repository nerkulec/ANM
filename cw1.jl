f32 = Float32(4.71)
f64 = Float64(4.71)
w16 = w(f16)
w32 = w(f32)
w64 = w(f64)
aw16 = aw(f16)
aw32 = aw(f32)
aw64 = aw(f64)
print(w16, '\n')
print(w32, '\n')
print(w64, '\n')
print(aw16, '\n')
print(aw32, '\n')
print(aw64, "\n\n")
